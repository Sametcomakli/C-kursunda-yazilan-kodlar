#define _CRT_SECURE_NO_WARNINGS

#include "student.h"
#include "nutility.h"
#include <stdio.h>
#include <stdlib.h>
#include <string.h>


Student* set_student_random(Student* p)
{
	p->m_no = rand();
	strcpy(p->m_name, rname());
	strcpy(p->m_surname, rsurname());
	strcpy(p->m_town, rtown());
	set_date_random(&p->m_date);
	return p;
}

//----------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------

void print_student(const Student* p)
{
	printf("%-5d %-16s %-18s %-16s ", p->m_no, p->m_name, p->m_surname, p->m_town);
	print_date(&p->m_date);
}

//----------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------

void print_student_(FILE *f, const Student* p)
{
	fprintf(f, "%-5d %-16s %-18s %-16s ", p->m_no, p->m_name, p->m_surname, p->m_town);
	print_date_(f, &p->m_date);
}


